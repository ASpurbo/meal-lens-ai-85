import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type",
};

serve(async (req) => {
  if (req.method === "OPTIONS") {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const { messages, userProfile } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    
    if (!LOVABLE_API_KEY) {
      throw new Error("LOVABLE_API_KEY is not configured");
    }

    const systemPrompt = `You are NutriMind's AI nutrition coach. You provide personalized nutrition guidance based on the user's profile and goals.

User Profile:
- Age: ${userProfile?.age || "Unknown"}
- Gender: ${userProfile?.gender || "Unknown"}
- Height: ${userProfile?.height_cm || "Unknown"} cm
- Weight: ${userProfile?.weight_kg || "Unknown"} kg
- Activity Level: ${userProfile?.activity_level || "Unknown"}
- Diet Goal: ${userProfile?.diet_goal || "maintain"}
- Language: ${userProfile?.language || "en"}

Your role:
1. Provide personalized calorie and macro recommendations based on their profile
2. Answer nutrition questions in a friendly, professional manner
3. Give practical meal suggestions and tips
4. Motivate and support users on their health journey
5. Be concise but helpful - aim for responses under 200 words
6. If they have a specific diet goal (lose weight, gain muscle, etc.), tailor advice accordingly
7. Respond in the user's preferred language when possible

Be encouraging but realistic. Base calculations on proven formulas (Mifflin-St Jeor for BMR, appropriate activity multipliers).`;

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-2.5-flash",
        messages: [
          { role: "system", content: systemPrompt },
          ...messages,
        ],
        stream: true,
      }),
    });

    if (!response.ok) {
      const errorText = await response.text();
      console.error("AI gateway error:", response.status, errorText);
      
      if (response.status === 429) {
        return new Response(JSON.stringify({ error: "Rate limit exceeded. Please try again in a moment." }), {
          status: 429,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      if (response.status === 402) {
        return new Response(JSON.stringify({ error: "AI credits exhausted. Please add credits to continue." }), {
          status: 402,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      
      return new Response(JSON.stringify({ error: "AI service temporarily unavailable" }), {
        status: 500,
        headers: { ...corsHeaders, "Content-Type": "application/json" },
      });
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (error) {
    console.error("AI coach error:", error);
    return new Response(JSON.stringify({ error: error instanceof Error ? error.message : "Unknown error" }), {
      status: 500,
      headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  }
});
